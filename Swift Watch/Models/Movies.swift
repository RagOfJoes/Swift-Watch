import Foundation

struct Movies: Codable, Hashable {
    let page: Int
    let totalPages: Int
    let results: [Movie]
    let totalResults: Int
    
    enum CodingKeys: String, CodingKey {
        case page
        case results
        case totalPages = "total_pages"
        case totalResults = "total_results"
    }
}
